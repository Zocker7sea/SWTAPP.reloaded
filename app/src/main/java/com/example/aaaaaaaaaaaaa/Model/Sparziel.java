package com.example.aaaaaaaaaaaaa.Model;

public class Sparziel {
    private int id;

    private String name;

    private float wert;

    public Sparziel(int id, String name, float wert) {
        this.id = id;
        this.name = name;
        this.wert = wert;
    }

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName() {
        this.name = name;
    }

    public float getWert() {
        return this.wert;
    }

    public void setWert(Float wert) {
        this.wert = wert;
    }
}
